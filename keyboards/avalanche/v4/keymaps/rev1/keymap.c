// Process record user, handle layers and special keys
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        meow = (meow + 1) % 3;
        if (meow != last_meow){
            last_meow = meow;
        }
        oled_task_user();
    }
    switch (keycode) {
        case LOWER:
            if (record->event.pressed) {
                layer_on(_LOWER);
                update_tri_layer(_LOWER, _RAISE, _TRI);
              } else {
                layer_off(_LOWER);
                update_tri_layer(_LOWER, _RAISE, _TRI);
              }
              return false;
              break;
        case RAISE:
            if (record->event.pressed) {
            layer_on(_RAISE);
            update_tri_layer(_LOWER, _RAISE, _TRI);
            } else {
            layer_off(_RAISE);
            update_tri_layer(_LOWER, _RAISE, _TRI);
            }
            return false;
            break;
        case S_ALT: // allows for special control of the encoders via is_salt
            if (record->event.pressed) {
                register_code(KC_LALT);
                is_salt = true;
            } else {
                unregister_code(KC_LALT);
                is_salt = false;
            }
            return false;
            break;
    }
    return true;
};
